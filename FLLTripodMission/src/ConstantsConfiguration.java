public class ConstantsConfiguration {
    private double LR, WR, PO1X, PO1Y, PO2X, PO2Y, PFX, PFY, LT;

    public ConstantsConfiguration(double LR, double WR, double PO1X, double PO1Y, double PO2X, double PO2Y, double PFX, double PFY, double LT) {
        this.LR = LR;
        this.WR = WR;
        this.PO1X = PO1X;
        this.PO1Y = PO1Y;
        this.PO2X = PO2X;
        this.PO2Y = PO2Y;
        this.PFX = PFX;
        this.PFY = PFY;
        this.LT = LT;
    }

    public double getLR() {
        return LR;
    }

    public void setLR(double LR) {
        this.LR = LR;
    }

    public double getWR() {
        return WR;
    }

    public void setWR(double WR) {
        this.WR = WR;
    }

    public double getPO1X() {
        return PO1X;
    }

    public void setPO1X(double PO1X) {
        this.PO1X = PO1X;
    }

    public double getPO1Y() {
        return PO1Y;
    }

    public void setPO1Y(double PO1Y) {
        this.PO1Y = PO1Y;
    }

    public double getPO2X() {
        return PO2X;
    }

    public void setPO2X(double PO2X) {
        this.PO2X = PO2X;
    }

    public double getPO2Y() {
        return PO2Y;
    }

    public void setPO2Y(double PO2Y) {
        this.PO2Y = PO2Y;
    }

    public double getPFX() {
        return PFX;
    }

    public void setPFX(double PFX) {
        this.PFX = PFX;
    }

    public double getPFY() {
        return PFY;
    }

    public void setPFY(double PFY) {
        this.PFY = PFY;
    }

    public double getLT() {
        return LT;
    }

    public void setLT(double LT) {
        this.LT = LT;
    }
}
